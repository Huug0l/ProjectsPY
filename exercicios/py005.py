## forma com varias variaveis , e armazenando
n1 = int(input('Digite um numero inteiro: '))
ant = n1 - 1
suc = n1 + 1
print(f'Analisando o valor inteiro {n1}, seu sucessor é {suc} e o sucessor é {ant} ')

## forma mais simples, sem armazenar

n1 = int(input('Digite um numero inteiro: '))
print(f'Analisando o valor inteiro {n1}, seu sucessor é {n1 + 1} e o sucessor é {n1 - 1} ')
